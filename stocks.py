class Stock:
    def __init__(self, name, sector, pe, revenueGrowth, prices):
        self.name = name
        self.sector = sector
        self.pe = pe
        self.revenueGrowth = revenueGrowth
        self.prices = prices

    def getReturn(self, startWeek, endWeek):
        startPrice = self.prices[startWeek]
        endPrice = self.prices[endWeek]
        return (endPrice - startPrice) / startPrice

    def getWeeklyReturns(self):
        returns = []
        for i in range(1, len(self.prices)):
            weekReturn = self.getReturn(i - 1, i)
            returns.append(weekReturn)
        return returns

    def getVolatility(self):
        returns = self.getWeeklyReturns()
        mean = sum(returns) / len(returns)
        squared = [(v - mean) ** 2 for v in returns]
        sdv = (sum(squared) / len(squared)) ** 0.5
        return sdv


